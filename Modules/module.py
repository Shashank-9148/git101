def power_of(a,b):
    '''This function calculates the power of a rise to b '''

    c = a**b
    print(c)


def get_quotient(numerator,denominator):
    '''This function calculates the numerator divided by denominator'''

    quotient = numerator/denominator
    print(quotient)

