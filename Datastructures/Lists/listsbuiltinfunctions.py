#Lists built in function

#using len,max,sum,min function
lst = [20,30,23,12,10]
print(len(lst))
print(max(lst))
print(min(lst))
print(sum(lst))


#using range function
lst = [2,3,4,5]
for i in range(len(lst)):
    print(i,lst[i])


#using enemurate function
lst = [1,2,3,4,5]
for i,j in enumerate(lst):
    print(i,j)


